async function loadBlogDetail() {
  const container = document.getElementById("singleBlog");
  if (!container) return;

  const params = new URLSearchParams(window.location.search);
  const blogId = params.get("id");
  if (!blogId) {
    container.innerHTML = "<p>Invalid blog link.</p>";
    return;
  }

  const blog = allBlogs.find(b => b.id === blogId);
  if (!blog) {
    container.innerHTML = "<p>Blog not found.</p>";
    return;
  }

    container.innerHTML = `
    <article class="blog-detail-article">
      <header class="blog-detail-header">
        <h1 class="blog-detail-title">${blog.title}</h1>
        <div class="blog-detail-meta">
          <span><i class="fas fa-user"></i> ${blog.author}</span>
          <span><i class="fas fa-calendar"></i> ${blog.date}</span>
        </div>
      </header>
      <div class="blog-detail-body">
        ${blog.image ? `<img class="blog-detail-image" src="${blog.image}" alt="${blog.title}">` : ""}
        <div class="blog-detail-content">${(blog.content || "").replace(/\n/g, "<br><br>")}</div>
        <a class="back-to-list" href="blog.html">‚Üê Back to Blogs</a>
      </div>
    </article>
  `;



  // Scroll to top
  window.scrollTo({ top: 0, behavior: "smooth" });
}

document.addEventListener("DOMContentLoaded", loadBlogDetail);
